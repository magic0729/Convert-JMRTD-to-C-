namespace org.jmrtd.lds
{
	public static class PACEInfo
	{
		public enum MappingType { GM, IM, CAM }

		public static MappingType ToMappingType(string oid)
		{
			return oid switch
			{
				"0.4.0.127.0.7.2.2.4.1.1" or
				"0.4.0.127.0.7.2.2.4.1.2" or
				"0.4.0.127.0.7.2.2.4.1.3" or
				"0.4.0.127.0.7.2.2.4.1.4" or
				"0.4.0.127.0.7.2.2.4.2.1" or
				"0.4.0.127.0.7.2.2.4.2.2" or
				"0.4.0.127.0.7.2.2.4.2.3" or
				"0.4.0.127.0.7.2.2.4.2.4" => MappingType.GM,
				"0.4.0.127.0.7.2.2.4.3.1" or
				"0.4.0.127.0.7.2.2.4.3.2" or
				"0.4.0.127.0.7.2.2.4.3.3" or
				"0.4.0.127.0.7.2.2.4.3.4" or
				"0.4.0.127.0.7.2.2.4.4.1" or
				"0.4.0.127.0.7.2.2.4.4.2" or
				"0.4.0.127.0.7.2.2.4.4.3" or
				"0.4.0.127.0.7.2.2.4.4.4" => MappingType.IM,
				"0.4.0.127.0.7.2.2.4.6.2" or
				"0.4.0.127.0.7.2.2.4.6.3" or
				"0.4.0.127.0.7.2.2.4.6.4" => MappingType.CAM,
				_ => throw new System.ArgumentException($"Unknown OID: {oid}")
			};
		}

		public static string ToKeyAgreementAlgorithm(string oid)
		{
			return oid switch
			{
				"0.4.0.127.0.7.2.2.4.1.1" or
				"0.4.0.127.0.7.2.2.4.1.2" or
				"0.4.0.127.0.7.2.2.4.1.3" or
				"0.4.0.127.0.7.2.2.4.1.4" or
				"0.4.0.127.0.7.2.2.4.3.1" or
				"0.4.0.127.0.7.2.2.4.3.2" or
				"0.4.0.127.0.7.2.2.4.3.3" or
				"0.4.0.127.0.7.2.2.4.3.4" => "DH",
				_ => "ECDH",
			};
		}

		public static string ToCipherAlgorithm(string oid)
		{
			return oid switch
			{
				"0.4.0.127.0.7.2.2.4.1.1" or
				"0.4.0.127.0.7.2.2.4.3.1" or
				"0.4.0.127.0.7.2.2.4.2.1" or
				"0.4.0.127.0.7.2.2.4.4.1" => "DESede",
				_ => "AES",
			};
		}

		public static string ToDigestAlgorithm(string oid)
		{
			return oid switch
			{
				"0.4.0.127.0.7.2.2.4.1.1" or
				"0.4.0.127.0.7.2.2.4.3.1" or
				"0.4.0.127.0.7.2.2.4.2.1" or
				"0.4.0.127.0.7.2.2.4.4.1" => "SHA-1",
				_ => "SHA-256",
			};
		}

		public static int ToKeyLength(string oid)
		{
			return oid switch
			{
				"0.4.0.127.0.7.2.2.4.1.1" or
				"0.4.0.127.0.7.2.2.4.3.1" or
				"0.4.0.127.0.7.2.2.4.2.1" or
				"0.4.0.127.0.7.2.2.4.4.1" or
				"0.4.0.127.0.7.2.2.4.1.2" or
				"0.4.0.127.0.7.2.2.4.3.2" or
				"0.4.0.127.0.7.2.2.4.2.2" or
				"0.4.0.127.0.7.2.2.4.4.2" or
				"0.4.0.127.0.7.2.2.4.6.2" => 128,
				"0.4.0.127.0.7.2.2.4.1.3" or
				"0.4.0.127.0.7.2.2.4.2.3" or
				"0.4.0.127.0.7.2.2.4.3.3" or
				"0.4.0.127.0.7.2.2.4.4.3" or
				"0.4.0.127.0.7.2.2.4.6.3" => 192,
				_ => 256,
			};
		}
	}
}


